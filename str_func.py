def str_to_capital_letters(s: str) -> str:
    """Функция принимает на вход строку и возвращает ее 
    со всеми заглавными буквами"""
    return s.upper()


def str_to_capital_first_letter(s: str) -> str:
    """функция принимает на вход строку и возвращает
    каждое слово с заглавной буквы"""
    return s.title()
